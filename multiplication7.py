def multiplication(value1,value2):
    result = value1 * value2
    return result
def main():
    print("Enter first number:")
    no1  = int(input())

    print("enter second number:")
    no2 = int(input())
    ans = multiplication(no1,no2)
    print("multiplication is:",ans)

if __name__ == "__main__":
        main()

